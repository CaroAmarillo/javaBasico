package com.ejercicio2;

public class Calculadora {

	//Metodos

	public int Sumar(int numero1, int numero2) {
		
		return numero1 + numero2;
		
	}

	public int Sumar(int numero1, int numero2, int numero3){
		
		return numero1 + numero2 + numero3;
	}

	public float Sumar(float numero4, float numero5){
		
		return numero4 + numero5;		
	}

	public float Sumar(float numero4, float numero5, float numero){
			
			return numero4 + numero5;		
		}
	
	public int Restar(int numero1, int numero2) {
			
			return numero1 - numero2;
			
		}
	
	public int Restar(int numero1, int numero2, int numero3){
		
		return numero1 - numero2 - numero3;
		
	}

	public float Restar(float numero4, float numero5){
		
		return numero4 - numero5;		
	}

	public float Restar(float numero4, float numero5, float numero){
			
			return numero4 - numero5;		
		}

}
